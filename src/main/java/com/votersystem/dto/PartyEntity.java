package com.votersystem.dto;

public class PartyEntity {
	
	private int partyId;
	private String partyname;
	private String presidentname;
	private String symboll;
	private String customV1;
	private String customV2;
	private int  customN1;
	private int  customN2;
	
	
	public int getPartyId() {
		return partyId;
	}
	public void setPartyId(int partyId) {
		this.partyId = partyId;
	}
	public String getPartyname() {
		return partyname;
	}
	public void setPartyname(String partyname) {
		this.partyname = partyname;
	}
	public String getPresidentname() {
		return presidentname;
	}
	public void setPresidentname(String presidentname) {
		this.presidentname = presidentname;
	}
	
	public String getSymboll() {
		return symboll;
	}
	public void setSymboll(String symboll) {
		this.symboll = symboll;
	}
	public String getCustomV1() {
		return customV1;
	}
	public void setCustomV1(String customV1) {
		this.customV1 = customV1;
	}
	public String getCustomV2() {
		return customV2;
	}
	public void setCustomV2(String customV2) {
		this.customV2 = customV2;
	}
	public int getCustomN1() {
		return customN1;
	}
	public void setCustomN1(int customN1) {
		this.customN1 = customN1;
	}
	public int getCustomN2() {
		return customN2;
	}
	public void setCustomN2(int customN2) {
		this.customN2 = customN2;
	}
	
	

}
